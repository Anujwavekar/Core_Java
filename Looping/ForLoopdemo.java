package Looping;

public class ForLoopdemo {
    public static void main(String[] args) {
        for(int i=1;i<=100;i++)
        {
            if(i%2==0)
            {
                System.out.printf("the number %d in even\n",i);
            }
            else
            {
                System.out.printf("the number %d in odd\n",i);
            }
        }
    }
    
}
